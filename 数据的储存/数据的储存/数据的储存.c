#include <stdio.h>
#include <string.h>

int main()
{
	int a = 9;
	//00000000 00000000 00000000 00001001 -补码
	//0 00000000 00000000000000000001001
	//(-1)^0 * 0.00000000000000000001001 *2^(-126)
	float* pFloat = (float*)&a;
	printf("a = %d\n", a);
	printf("pa = %f\n", *pFloat);
	*pFloat = 9.0;
	//1001.0
	//(-1)^0 * 1.001 * 2^3
	//整型打印出来为 1091567616	
	printf("a = %d\n", a);
	printf("pa = %f\n", *pFloat);
}

